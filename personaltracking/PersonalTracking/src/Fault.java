
public class Fault {
	
	private int faultID;
	private String faultUnit;
	
	public int getFaultID() {
		return faultID;
	}
	public void setFaultID(int faultID) {
		this.faultID = faultID;
	}
	public String getFaultUnit() {
		return faultUnit;
	}
	public void setFaultUnit(String faultUnit) {
		this.faultUnit = faultUnit;
	}
	
	
	
}
