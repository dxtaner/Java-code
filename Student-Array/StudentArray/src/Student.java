
public class Student {
	
	private int stdNo;
	private String stdName;
	private String stdSurname;
	private String stdDepartment;
	
	public int getStdNo() {
		return stdNo;
	}
	public void setStdNo(int stdNo) {
		this.stdNo = stdNo;
	}
	public String getStdName() {
		return stdName;
	}
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	public String getStdSurname() {
		return stdSurname;
	}
	public void setStdSurname(String stdSurname) {
		this.stdSurname = stdSurname;
	}
	public String getStdDepartment() {
		return stdDepartment;
	}
	public void setStdDepartment(String stdDepartment) {
		this.stdDepartment = stdDepartment;
	}
	
	
	
}
